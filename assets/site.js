const defaultLanguage = "en";
const publicRepoCount = 97;
let cachedRepos = null;

function getStoredLanguage() {
  return localStorage.getItem("site-language") || defaultLanguage;
}

function getStoredTheme() {
  return localStorage.getItem("site-theme") || "system";
}

function setTheme(theme) {
  const prefersDark = window.matchMedia("(prefers-color-scheme: dark)").matches;
  const useDark = theme === "dark" || (theme === "system" && prefersDark);
  document.documentElement.classList.toggle("dark", useDark);
  document.documentElement.classList.toggle("light", !useDark);
  localStorage.setItem("site-theme", theme);
  document.querySelectorAll("[data-theme-select]").forEach((select) => {
    select.value = theme;
  });
}

function setLanguage(language) {
  const dictionary = translations[language] || translations[defaultLanguage];
  document.documentElement.lang = language;
  localStorage.setItem("site-language", language);
  document.querySelectorAll("[data-language-select]").forEach((select) => {
    select.value = language;
  });
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = dictionary[node.dataset.i18n];
    if (value) {
      node.textContent = value;
    }
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((node) => {
    const value = dictionary[node.dataset.i18nAria];
    if (value) {
      node.setAttribute("aria-label", value);
    }
  });
  renderRepoList();
}

function initControls() {
  document.querySelectorAll("[data-theme-select]").forEach((select) => {
    select.addEventListener("change", () => setTheme(select.value));
  });
  document.querySelectorAll("[data-language-select]").forEach((select) => {
    select.addEventListener("change", () => setLanguage(select.value));
  });
  window.matchMedia("(prefers-color-scheme: dark)").addEventListener("change", () => {
    if (getStoredTheme() === "system") {
      setTheme("system");
    }
  });
  setTheme(getStoredTheme());
  setLanguage(getStoredLanguage());
}

function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach((link) => {
    link.addEventListener("click", (event) => {
      const target = document.querySelector(link.getAttribute("href"));
      if (!target) {
        return;
      }
      event.preventDefault();
      target.scrollIntoView({ behavior: "smooth", block: "start" });
      history.pushState(null, "", link.getAttribute("href"));
    });
  });
}

async function hydrateRepoCount() {
  const repoCountNodes = document.querySelectorAll("[data-repo-count]");
  if (!repoCountNodes.length) {
    return;
  }
  repoCountNodes.forEach((node) => {
    node.textContent = String(publicRepoCount);
  });
  try {
    const response = await fetch("https://api.github.com/users/fidele007");
    if (!response.ok) {
      throw new Error("GitHub profile unavailable");
    }
    const profile = await response.json();
    if (Number.isFinite(profile.public_repos)) {
      repoCountNodes.forEach((node) => {
        node.textContent = String(profile.public_repos);
      });
    }
  } catch (error) {
    console.warn(error);
  }
}

function repoCard(repo) {
  const updated = new Date(repo.updated_at).toLocaleDateString(document.documentElement.lang || "en", {
    year: "numeric",
    month: "short",
    day: "numeric"
  });
  const language = repo.language ? `<span class="font-label text-[10px] border border-primary px-2 py-0.5 uppercase font-bold">${repo.language}</span>` : "";
  const topics = (repo.topics || []).slice(0, 3).map((topic) => `<span class="font-label text-[10px] border border-primary px-2 py-0.5 uppercase font-bold">${topic}</span>`).join("");
  const dictionary = translations[getStoredLanguage()] || translations[defaultLanguage];
  return `
    <article class="border-2 border-primary bg-white p-5 navy-shadow hover:translate-x-1 hover:translate-y-1 hover:shadow-none transition-all flex flex-col gap-4">
      <div>
        <h2 class="font-headline text-xl font-black uppercase break-words">${repo.name}</h2>
        <p class="mt-3 text-on-surface-variant text-sm font-medium leading-relaxed">${repo.description || ""}</p>
      </div>
      <div class="flex flex-wrap gap-2 mt-auto">${language}${topics}</div>
      <div class="flex items-center justify-between gap-4 border-t border-outline-variant pt-4">
        <span class="font-label text-[10px] uppercase font-bold text-on-surface-variant">${dictionary["works.updated"]}: ${updated}</span>
        <a class="font-label text-[10px] uppercase font-bold text-secondary hover:text-primary" href="${repo.html_url}" target="_blank" rel="noreferrer">${dictionary["works.source"]}</a>
      </div>
    </article>
  `;
}

function renderRepoList() {
  const list = document.querySelector("[data-repo-list]");
  if (!list || !cachedRepos) {
    return;
  }
  const dictionary = translations[getStoredLanguage()] || translations[defaultLanguage];
  list.innerHTML = cachedRepos.length ? cachedRepos.map(repoCard).join("") : `<p data-i18n="works.empty">${dictionary["works.empty"]}</p>`;
}

async function initReposPage() {
  const list = document.querySelector("[data-repo-list]");
  if (!list) {
    return;
  }
  const dictionary = translations[getStoredLanguage()] || translations[defaultLanguage];
  try {
    const response = await fetch("https://api.github.com/users/fidele007/repos?per_page=100&sort=updated&type=public");
    if (!response.ok) {
      throw new Error("GitHub repositories unavailable");
    }
    cachedRepos = await response.json();
    renderRepoList();
  } catch (error) {
    console.warn(error);
    list.innerHTML = `<p class="border-2 border-primary bg-white p-6 navy-shadow" data-i18n="works.error">${dictionary["works.error"]}</p>`;
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initControls();
  initSmoothScroll();
  hydrateRepoCount();
  initReposPage();
});
